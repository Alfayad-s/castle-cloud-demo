export function getMachineById(id: string): Machine | undefined {
  return machines.find((machine) => machine.id === id);
}

export function getMachinesBySite(site: string): Machine[] {
  return machines.filter((machine) => machine.currentSite === site);
}

export function getMachinesByStatus(status: Machine["status"]): Machine[] {
  return machines.filter((machine) => machine.status === status);
}

export function getMachineWeeklyUsage(machineId: string) {
  const seed = machineId.charCodeAt(machineId.length - 1);
  const base = 4 + (seed % 4);

  return [
    { day: "Mon", hours: base + 2 },
    { day: "Tue", hours: base + 3 },
    { day: "Wed", hours: base + 1 },
    { day: "Thu", hours: base + 4 },
    { day: "Fri", hours: base + 2 },
    { day: "Sat", hours: base },
    { day: "Sun", hours: 0 },
  ];
}
